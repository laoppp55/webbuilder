package com.bizwink.log;

public class PublishArticleCountOfColumn {
    private int columnid;
    private int pcolumnid;
    private String colname;
    private int subcolnum;
    private int level;
    private int articlecount;

    public int getColumnid() {
        return columnid;
    }

    public void setColumnid(int columnid) {
        this.columnid = columnid;
    }

    public int getPcolumnid() {
        return pcolumnid;
    }

    public void setPcolumnid(int pcolumnid) {
        this.pcolumnid = pcolumnid;
    }

    public String getColname() {
        return colname;
    }

    public void setColname(String colname) {
        this.colname = colname;
    }

    public int getSubcolnum() {
        return subcolnum;
    }

    public void setSubcolnum(int subcolnum) {
        this.subcolnum = subcolnum;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public int getArticlecount() {
        return articlecount;
    }

    public void setArticlecount(int articlecount) {
        this.articlecount = articlecount;
    }
}
